ll kadane(vl& a)
{
	ll size=a.size();
   ll max_so_far = a[0];
   ll curr_max = a[0];
 
   for (ll i = 1; i < size; i++)
   {
        curr_max = max(a[i], curr_max+a[i]);
        max_so_far = max(max_so_far, curr_max);
   }
   return max_so_far;
}

